const computeGradient = (
  values: Float32Array,
  nx: number,
  ny: number,
  nz: number,
  cellSize: { x: number; y: number; z: number },
  x: number,
  y: number,
  z: number
) => {
  const clamp = (value: number, max: number) => Math.max(0, Math.min(max, value));
  const idx = (xi: number, yi: number, zi: number) =>
    clamp(xi, nx - 1) + clamp(yi, ny - 1) * nx + clamp(zi, nz - 1) * nx * ny;

  const dx = (values[idx(x + 1, y, z)] - values[idx(x - 1, y, z)]) / (2 * cellSize.x);
  const dy = (values[idx(x, y + 1, z)] - values[idx(x, y - 1, z)]) / (2 * cellSize.y);
  const dz = (values[idx(x, y, z + 1)] - values[idx(x, y, z - 1)]) / (2 * cellSize.z);

  const len = Math.sqrt(dx * dx + dy * dy + dz * dz);
  if (len < 1e-6) return { x: 0, y: 1, z: 0 };
  return { x: dx / len, y: dy / len, z: dz / len };
};

